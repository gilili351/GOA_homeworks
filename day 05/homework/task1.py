# 2
num1 = (input("Enter first number: "))
num2 = (input("Enter second number: "))  

# მათემატიკური ოპერაციები
print(f"Addition: {num1 + num2}")  # მიმატება
print(f"Subtraction: {num1 - num2}")  # გამოკლება
print(f"Multiplication: {num1 * num2}")  # გამრავლება
print(f"Division: {num1 / num2}")  # გაყოფა
print(f"Modulo: {num1 % num2}")  # ნაშთიანი გაყოფა
print(f"Power: {num1 ** num2}")  # ხარისხში აყვანა

# შედარების ოპერაციები
print(f"{num1} > {num2}: {num1 > num2}")  # მეტობა
print(f"{num1} < {num2}: {num1 < num2}")  # ნაკლებობა
print(f"{num1} >= {num2}: {num1 >= num2}")  # მეტი ან ტოლი
print(f"{num1} <= {num2}: {num1 <= num2}")  # ნაკლები ან ტოლი
print(f"{num1} == {num2}: {num1 == num2}")  # ტოლობა
print(f"{num1} != {num2}: {num1 != num2}")  # უთანასწორობა

